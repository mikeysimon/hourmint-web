create extension if not exists "pgcrypto";

create table if not exists public.clients (
  id bigint generated by default as identity primary key,
  name text not null unique,
  email text,
  company text,
  notes text,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.projects (
  id bigint generated by default as identity primary key,
  client_id bigint not null references public.clients(id) on delete cascade,
  name text not null,
  rate numeric(12, 2) not null,
  description text,
  active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.invoices (
  id bigint generated by default as identity primary key,
  invoice_number text not null unique,
  client_id bigint not null references public.clients(id) on delete restrict,
  generated_at timestamptz not null default timezone('utc', now()),
  detail_level text not null check (detail_level in ('summary', 'project', 'detailed')),
  subtotal numeric(12, 2) not null default 0,
  pdf_path text not null,
  summary_pdf_path text,
  project_pdf_path text,
  detailed_pdf_path text,
  status text not null default 'unpaid' check (status in ('paid', 'unpaid')),
  paid_at timestamptz
);

create table if not exists public.time_entries (
  id bigint generated by default as identity primary key,
  project_id bigint not null references public.projects(id) on delete cascade,
  start_at timestamptz not null,
  end_at timestamptz not null,
  hours numeric(8, 2) not null,
  description text not null,
  invoiced boolean not null default false,
  invoice_id bigint references public.invoices(id) on delete set null,
  created_at timestamptz not null default timezone('utc', now()),
  check (end_at > start_at)
);

create table if not exists public.settings (
  key text primary key,
  value text not null
);

