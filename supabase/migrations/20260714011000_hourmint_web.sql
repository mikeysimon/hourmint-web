create or replace function public.reset_hourmint_sequences()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  perform setval(pg_get_serial_sequence('public.clients', 'id'), greatest((select coalesce(max(id), 1) from public.clients), 1), true);
  perform setval(pg_get_serial_sequence('public.projects', 'id'), greatest((select coalesce(max(id), 1) from public.projects), 1), true);
  perform setval(pg_get_serial_sequence('public.invoices', 'id'), greatest((select coalesce(max(id), 1) from public.invoices), 1), true);
  perform setval(pg_get_serial_sequence('public.time_entries', 'id'), greatest((select coalesce(max(id), 1) from public.time_entries), 1), true);
end;
$$;

grant execute on function public.reset_hourmint_sequences() to authenticated, service_role;
